package util;

public class ReviewBean {

	private int Review_no_;
    private String Review_title;
    private String Review_review;
    private String Review_name;
    
    
	public int getReview_no() {
		return Review_no_;
	}
	public void setReview_no(int review_no_) {
		Review_no_ = review_no_;
	}
	public String getReview_title() {
		return Review_title;
	}
	public void setReview_title(String review_title) {
		Review_title = review_title;
	}
	public String getReview_review() {
		return Review_review;
	}
	public void setReview_review(String review_review) {
		Review_review = review_review;
	}
	public String getReview_name() {
		return Review_name;
	}
	public void setReview_name(String review_name) {
		Review_name = review_name;
	}
    
    
}
